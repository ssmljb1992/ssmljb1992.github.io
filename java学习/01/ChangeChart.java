public class ChangeChart{
	public static void main(String[] args){
		//\t: 一个制表符，实现对齐的功能 
		System.out.println("北京\t上海\t深圳");
		//\n: 换行符
		System.out.println("jack\nsmith\nmarry");
		//3,\\ :一个\
		System.out.println("E:\\java\\hsp\\01");
		//4,\" :"
		System.out.println("老师说:\"要好好学习java，有前途\"");
		//5,\':'
		System.out.println("老师说:\'要好好学习java，有前途\'");
		//6,\r:一个回车
		System.out.println("教育乃是国之根本\r北京");
	}
}